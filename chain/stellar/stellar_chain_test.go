func TestChain_ReadOnly(t *testing.T) {
	t.Parallel()

	ctfConfig := provider.CTFChainProviderConfig{
		Once:               &sync.Once{},
		DeployerKeypairGen: provider.KeypairRandom(),
		Image:              "stellar/quickstart:nightly", // as of Apr 2026, nightly spawns friendbot *much* faster
	}
	chainSelector := chainsel.STELLAR_LOCALNET.Selector
	chain, err := provider.NewCTFChainProvider(t, chainSelector, ctfConfig).Initialize(t.Context())
	require.NoError(t, err)

	stellarChain, ok := chain.(stellar.Chain)
	require.True(t, ok)
	fundViaFriendbot(t, stellarChain.FriendbotURL, stellarChain.Signer.Address())

	roChain, err := chain.ReadOnly()
	require.NoError(t, err)
	roStellarChain, ok := roChain.(stellar.Chain)
	require.True(t, ok)

	// write with read-write client should work
	deployer := NewDeployer(stellarChain.Client, stellarChain.NetworkPassphrase, stellarChain.Signer.KeypairFull())
	contractID, err := deployer.DeployContract(t.Context(), registryContractBytes, [32]byte{})
	require.NoError(t, err)

	// read with read-only client should work
	args := []xdr.ScVal{{Type: xdr.ScValTypeScvString, Str: pointer.To(xdr.ScString("world"))}}
	roDeployer := NewDeployer(roStellarChain.Client, roStellarChain.NetworkPassphrase, roStellarChain.Signer.KeypairFull())
	_, err = roDeployer.SimulateContract(t.Context(), contractID, "hello", args)
	require.NoError(t, err)

	// write with read-only client should fail
	_, err = roDeployer.DeployContract(t.Context(), registryContractBytes, [32]byte{})
	require.ErrorContains(t, err, "transaction rejected:")
}

// ----- helpers -----

func fundViaFriendbot(t *testing.T, friendbotURL, address string) {
	t.Helper()

	faucetURL := friendbotURL + "?addr=" + url.QueryEscape(address)
	require.Eventually(t, func() bool {
		req, err := http.NewRequestWithContext(t.Context(), http.MethodGet, faucetURL, nil)
		require.NoError(t, err)
		resp, err := http.DefaultClient.Do(req)
		require.NoError(t, err)
		defer resp.Body.Close()

		return resp.StatusCode == http.StatusOK
	}, 60*time.Second, 1000*time.Millisecond)
}

